#include "libft.h"

int		ft_countspace(char *str)
{
	int	i;
	int result;

	i = 0;
	result = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ' ')
			result++;
		i++;
	}
	return (result);
}
