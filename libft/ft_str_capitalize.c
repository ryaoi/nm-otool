#include "ft_printf.h"

void		ft_str_capitalize(char **str)
{
	int		i;

	i = 0;
	while ((*str)[i] != '\0')
	{
		((*str)[i]) = ft_toupper((int)((*str)[i]));
		i++;
	}
}
