#include "ft_nm.h"

uint16_t		swap16(u_int16_t origin)
{
	return ((origin >> 8) | (origin << 8));
}

uint32_t		swap32(u_int32_t origin)
{
	return (((origin >> 24) & 0xff)
				| ((origin >> 8) & 0xff00)
				| ((origin << 8) & 0xff0000)
				| ((origin << 24) & 0xff000000));
}
